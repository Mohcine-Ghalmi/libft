#include "libft.h"

void	*ft_calloc(size_t count, size_t size)
{
	char	*str;

	str = malloc(count * size);
	if (!str || (count == 9223372036854775807L && size == 9223372036854775807L))
		return (0);
	ft_bzero(str, count * size);
	return (str);
}
